package com.github.oohira.intercom.model;

public class Avatar {

	private String type;
	private String imageUrl;
	
	public Avatar() {
	}
	
	public String getType() {
		return type;
	}
	
	public String getImageUrl() {
		return imageUrl;
	}
	
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	
}
